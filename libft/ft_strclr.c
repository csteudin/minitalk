#include "libft.h"

void	ft_strclr(char *s)
{
	int	i;
	int	len;

	if (!s)
		return ;
	i = 0;
	len = ft_strlen(s);
	while (i < len + 2)
	{
		s[i] = 0;
		i++;
	}
}
